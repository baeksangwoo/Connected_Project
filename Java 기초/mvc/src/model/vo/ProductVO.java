package model.vo;

public class ProductVO {

	private int banana;
	private int apple;
	private int orange;
	
	public int getBanana() {
		return banana;
	}
	public void setBanana(int banana) {
		this.banana += banana;
	}
	public int getApple() {
		return apple;
	}
	public void setApple(int apple) {
		this.apple += apple;
	}
	public int getOrange() {
		return orange;
	}
	public void setOrange(int orange) {
		this.orange += orange;
	}
	
	

}
